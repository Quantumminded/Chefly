      <section>
        <h2 class="font-serif text-3xl text-white mb-4">Define Your Vision</h2>
        <p>
          The foundation of any exceptional private dinner lies in clarity of vision. Before selecting your chef or finalizing menu concepts, take time to articulate the purpose of your gathering. Are you celebrating an anniversary? Impressing business clients? Gathering intimate friends for an unforgettable evening? Your answer shapes everything—from atmosphere and cuisine style to service flow and presentation.
        </p>
        <p class="mt-4">
          Consider the season and your villa's natural aesthetic. A summer lakeside dinner on Lake Como calls for lighter, Mediterranean-inspired dishes with fresh local ingredients, while a winter celebration might showcase richer, more complex flavor profiles. Align your culinary vision with your setting.
        </p>
      </section>

      <section>
        <h2 class="font-serif text-3xl text-white mb-4">Guest Selection & Logistics</h2>
        <p>
          The composition of your guest list fundamentally influences menu decisions, seating dynamics, and service approach. A multi-generational family gathering with young children requires different culinary accommodations than a sophisticated dinner for executives. Communicate dietary preferences, allergies, and restrictions early—your private chef needs this information to craft a menu that honors every guest.
        </p>
        <p class="mt-4">
          Consider logistics: table layout, kitchen access, parking, and timing. Does your villa kitchen require equipment rentals? Will you need additional service staff? Professional private chefs handle these details seamlessly, transforming complexity into effortless hospitality.
        </p>
      </section>

      <section>
        <h2 class="font-serif text-3xl text-white mb-4">Menu Curation & Wine Pairing</h2>
        <p>
          This is where artistry meets precision. Rather than selecting dishes from a template menu, collaborate with your chef to craft a progression specifically tailored to your guests. Does someone have a passion for seafood? A preference for Italian classics? A celebration of seasonal ingredients?
        </p>
        <p class="mt-4">
          Wine pairing elevates the experience exponentially. Whether you're leveraging your villa's existing wine collection or working with a sommelier, thoughtful pairings transform a good dinner into an unforgettable one. Premium private chefs often coordinate sommelier service, ensuring each course is accompanied by the perfect glass.
        </p>
      </section>

      <section>
        <h2 class="font-serif text-3xl text-white mb-4">Service & Atmosphere</h2>
        <p>
          Impeccable service is invisible—it anticipates needs without intrusion. Your private chef and service team should understand the tone you're setting: formal and refined, or warm and casual? The choreography of plating, pacing between courses, and attention to detail all contribute to the overall experience.
        </p>
        <p class="mt-4">
          Consider ambiance: candlelight, floral arrangements, curated music. These elements create a sensory experience that, combined with exceptional cuisine, become the memories your guests treasure for years.
        </p>
      </section>

      <section>
        <h2 class="font-serif text-3xl text-white mb-4">Final Thoughts</h2>
        <p>
          Planning a perfect private dinner requires attention to detail, clear communication, and the right team. By defining your vision, understanding your guests, curating thoughtfully, and orchestrating seamless service, you create an evening that transcends dining—it becomes an experience that defines hospitality itself.
        </p>
      </section>
